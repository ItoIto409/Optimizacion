print(f"\n   Semana 2:")
print(f"   S2 = 0.6({S1:.1f}) + 0.2({M1:.1f}) + 40 = {S2:.2f}")
print(f"   M2 = 0.1({S1:.1f}) + 0.5({M1:.1f}) + 20 = {M2:.2f}")

# d) Sistema de equilibrio
print("\nd) Sistema de equilibrio (S* y M*):")
print("   Igualando S_{n+1}=S* y M_{n+1}=M*:")
print()
print("   0.4·S* - 0.2·M* = 40")
print("  -0.1·S* + 0.5·M* = 20")

A_eq = np.array([[0.4, -0.2],
                  [-0.1, 0.5]])
b_eq = np.array([40, 20])
equilibrio = np.linalg.solve(A_eq, b_eq)

