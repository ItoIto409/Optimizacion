print('  Verificación simulación (40 años):')
print('-' * 55)
print(f"  P0=400 (sobre umbral) → año 40: {P_sobre[-1]:.1f} peces")
print(f"  P0=250 (bajo umbral)  → año 40: {P_bajo[-1]:.1f} peces (extinción)")
