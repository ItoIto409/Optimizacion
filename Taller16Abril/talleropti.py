# c) Cálculo semanas 1 y 2
S0, M0 = 200, 80

def siguiente_semana(S, M):
    S_next = 0.6*S + 0.2*M + 40
    M_next = 0.1*S + 0.5*M + 20
    return S_next, M_next

S1, M1 = siguiente_semana(S0, M0)
S2, M2 = siguiente_semana(S1, M1)

print("\nc) Cálculo de semanas 1 y 2:")
print(f"\n   Semana 0: S0 = {S0},  M0 = {M0}")
print(f"\n   Semana 1:")
print(f"   S1 = 0.6({S0}) + 0.2({M0}) + 40 = {0.6*S0} + {0.2*M0} + 40 = {S1:.1f}")
