print('  PROBLEMA 4: MODELO LOGÍSTICO DE PESCA')
print('=' * 55)

r_fish = 0.4
K      = 1000
H      = 80
P0     = 400

# a) Variables y parámetros
print("\na) Variables y parámetros:")
print("   Variable dependiente  : P_n — población al final del año n")
print("   Variable independiente: n   — año (discreto, entero)")
print(f"   r = {r_fish}  (tasa de crecimiento intrínseca)")
print(f"   K = {K}  (capacidad máxima de la laguna, peces)")
print(f"   H = {H}    (peces extraídos por año)")

# b) No linealidad y discreto
print("\nb) No lineal y discreto:")
print("   No lineal : el término r·P_n·(1 - P_n/K) contiene P_n².")
print("   Discreto  : n toma valores enteros (año 1, 2, 3, ...).")

# c) P1 y P2
def siguiente_año(P):
    return P + r_fish*P*(1 - P/K) - H

P1 = siguiente_año(P0)
P2 = siguiente_año(P1)

print(f"\nc) Cálculo de P1 y P2:")
print(f"\n   P0 = {P0}")
print(f"   P1 = {P0} + 0.4·{P0}·(1 - {P0}/{K}) - {H}")
print(f"      = {P0} + {r_fish*P0:.1f}·{1 - P0/K:.1f} - {H} = {P1:.2f}")
print(f"\n   P2 = {P1:.2f} + 0.4·{P1:.2f}·(1 - {P1:.2f}/{K}) - {H}")
print(f"      = {P2:.4f}")

# d) Puntos de equilibrio
print("\nd) Puntos de equilibrio  (P_{n+1} = P_n = P*):")
print("   r·P*·(1 - P*/K) - H = 0")
print("   0.4·P*·(1 - P*/1000) = 80")
print("   P*² - 1000·P* + 200000 = 0")

a_coef = 1
b_coef = -1000
c_coef = 200000
discriminante = b_coef**2 - 4*a_coef*c_coef
P_eq1 = (-b_coef + np.sqrt(discriminante)) / (2*a_coef)
P_eq2 = (-b_coef - np.sqrt(discriminante)) / (2*a_coef)

print(f"\n   Discriminante = {discriminante}")
print(f"   P*₁ = (1000 + √{discriminante}) / 2 = {P_eq1:.2f} peces  (estable)")
print(f"   P*₂ = (1000 - √{discriminante}) / 2 = {P_eq2:.2f} peces  (inestable)")

# e) Interpretación
print("\ne) Interpretación física:")
print(f"   P*₁ ≈ {P_eq1:.0f} peces → Equilibrio ESTABLE.")
print(f"          La reproducción y la pesca se compensan.")
print(f"          Con P0={P0} > {P_eq2:.0f}, la población converge aquí.")
print(f"\n   P*₂ ≈ {P_eq2:.0f} peces → Equilibrio INESTABLE (umbral crítico).")
print(f"          Si P0 < {P_eq2:.0f}: reproducción < cosecha → extinción.")
print(f"          Si P0 > {P_eq2:.0f}: población converge a ~{P_eq1:.0f}.")

# Simulación para verificar
años = 40
P_sobre = [P0]
P_bajo  = [250]   # por debajo del umbral
P_s, P_b = P0, 250
for _ in range(años):
    P_s = max(siguiente_año(P_s), 0)
    P_b = max(siguiente_año(P_b), 0)
    P_sobre.append(P_s)
    P_bajo.append(P_b)

print()
