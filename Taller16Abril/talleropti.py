beta  = 0.00003
gamma = 0.1
N     = 10000
I0    = 50

# a) Variables y parámetros
print("\na) Variables y parámetros:")
print("   Variable dependiente  : I(t) — infectados en tiempo t")
print("   Variable independiente: t — tiempo en días")
print(f"   β  = {beta}  (tasa de contagio por día)")
print(f"   γ  = {gamma}    (tasa de recuperación por día)")
print(f"   N  = {N}  (población total constante)")

# b) No linealidad
print("\nb) No linealidad:")
print("   El término β·(N-I)·I contiene I² al expandirse:")
print("   β·N·I - β·I²  → el término I² hace el modelo NO LINEAL.")

# c) Condición inicial
print(f"\nc) Condición inicial:  I(0) = {I0} personas infectadas")

# d) Puntos de equilibrio
print("\nd) Puntos de equilibrio  (dI/dt = 0):")
print("   β·(N-I*)·I* - γ·I* = 0")
print("   I*·[ β·(N-I*) - γ ] = 0")
print()

I_eq1 = 0
# β(N - I*) = γ  →  I* = N - γ/β
I_eq2 = N - gamma / beta

print(f"   Equilibrio 1: I* = {I_eq1}")
print(f"   Equilibrio 2: I* = N - γ/β = {N} - {gamma}/{beta} = {I_eq2:.2f}")

# e) Interpretación
print("\ne) Interpretación física:")
print(f"   I* = 0       → Libre de enfermedad (extinción). Inestable")
print(f"                  si β·N > γ (R0 = β·N/γ = {beta*N/gamma:.1f} > 1).")
print(f"   I* ≈ {I_eq2:.0f}  → Endémico estable. Contagios = recuperaciones.")
print(f"                  Con I(0)=50, la epidemia converge a ~{I_eq2:.0f}.")

# Simulación numérica con Euler
dt = 0.1
t_max = 200
t_vals = np.arange(0, t_max, dt)
I_vals = [I0]
I_curr = I0
for _ in t_vals[1:]:
    dI = beta*(N - I_curr)*I_curr - gamma*I_curr
    I_curr = I_curr + dI*dt
    I_vals.append(I_curr)

print()
print('-' * 55)
print('  Verificación numérica (Euler):')
print('-' * 55)
print(f"  I(t=50)  = {I_vals[int(50/dt)]:.1f}")
